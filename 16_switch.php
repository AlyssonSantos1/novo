<?php

$identidade = 10;

switch ($identidade) {
    case $identidade == 18:
        echo 'dezoito';
    break;
    
    case $identidade == 17:
        echo 'dezessete';
    break;

    case $identidade == 16:
        echo 'dezesseis';
    break;

    case $identidade == 15:
        echo 'quinze';
    break;

    default:
        echo 'valor nao encontrado';
    break;
}



// switch serve para verificar uma condicao de uma variavel e é + indicado quando o codigo,
// é um pouco maior se comparado ao if else
// break é para se ele entrar no laço e for aquela condiçao o programa parar de executar,
// uma quebra
